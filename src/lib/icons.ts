/**
 * Get an icon component by name
 * Returns the icon if found, or a fallback icon (Sparkles/Palette)
 */
export function getIconByName(iconName: string, fallback: LucideIcon = Sparkles): LucideIcon {
    return ICON_MAP[iconName] || fallback;
}

/**
 * Check if an icon exists in the map
 */
export function hasIcon(iconName: string): boolean {
    return iconName in ICON_MAP;
}

/**
 * Get all available icon names
 */
export function getAvailableIconNames(): string[] {
    return Object.keys(ICON_MAP);
}

// Re-export the default fallback icons
export { Sparkles, Palette };

// Export the type for use in components
export type { LucideIcon, LucideProps };
